def addition(a,b):
    total = a + b
    print(a,"+",b,"=",total)

def subtraction(a,b):
    total = a - b
    print(a,"-",b,"=",total)

def multiplication(a,b):
    total = a * b
    print(a,"*",b,"=",total)

def division(a,b):
    total = a / b
    print(a,":",b,"=",total)

def increment(a):
    total = a + 1
    print(a,"+ 1 =",total)

def decrement(a):
    total = a - 1
    print(a,"- 1 =",total)

